"use strict";
// classes
class Invoice {
    constructor(c, d, a) {
        this.client = c;
        this.details = d;
        this.amount = a;
    }
    format() {
        return `${this.client} owes \$${this.amount} for ${this.details}`;
    }
}
const invOne = new Invoice('bayo', 'bandek blog', 300);
let invoices = [];
invoices.push(invOne);
console.log(invOne.format());
